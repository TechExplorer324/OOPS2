/**
 * Enumeration for different types of vehicles.
 * This enum defines various vehicle categories that can be used within the system.
 * Each constant represents a specific type of vehicle.
 */
enum VehicleType {
    CAR,              // Represents a standard car (e.g., sedan, hatchback)
    BIKE,             // Represents a motorcycle or scooter
    TRUCK,            // Represents a large vehicle, typically for transporting goods
    ELECTRIC_VEHICLE  // Represents a vehicle powered by electricity (e.g., electric car, e-bike)
}
